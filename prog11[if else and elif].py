#if else and elif(its the python equivalent of else if)

x=21

if x==2:
    print("x is 2")
elif x==3:
    print("x is 3")
else:
    print("x is not 2 or 3")
#syntax
# <if> <condition> <colon>
#       <body>
#<elif> <conditon> <colon>
#       <body>
#<else> <colon>
#        <body>
#   note:there can only be one if and one else, but there can be many elif between the two
